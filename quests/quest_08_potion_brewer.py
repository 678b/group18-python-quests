# Quest 8: Calculate total cost of items

dragon_scales = 3
price_per_scale = 10

elf_tears = 5
price_per_tear = 3

total_cost = (dragon_scales * price_per_scale) + (elf_tears * price_per_tear)

print("Dragon scales cost:", dragon_scales * price_per_scale, "gold")
print("Elf tears cost:", elf_tears * price_per_tear, "gold")
print("Total cost:", total_cost, "gold")